

public class Assignmentoperator {
    public static void main(String[] args) {
        int a=100, b=75, c=60, d=30, e=200;
        System.out.println(a+" "+b+" "+c+" "+d+" "+e);
         
        a+=10;
        b-=22;
        c*=6;
        d/=5;
        e%=10;



        System.out.println(a+" "+b+" "+c+" "+d+" "+e);
    }
}
